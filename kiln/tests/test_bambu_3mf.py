class TestResolveStartGcode:
    """Temperature substitution in start gcode templates.

    220°C and 65°C are parametric (PLA defaults).
    140°C, 170°C, 250°C, 25°C are fixed init temps that must NOT change.
    """

    def test_default_temps_unchanged(self):
        result = _resolve_start_gcode(
            FAKE_START_GCODE, hotend_temp=220, bed_temp=65
        )
        assert "M104 S220" in result
        assert "M140 S65" in result

    def test_custom_hotend_temp_replaces_220(self):
        result = _resolve_start_gcode(
            FAKE_START_GCODE, hotend_temp=250, bed_temp=65
        )
        assert "M104 S250" in result
        assert "M109 S250" in result

    def test_custom_bed_temp_replaces_65(self):
        result = _resolve_start_gcode(
            FAKE_START_GCODE, hotend_temp=220, bed_temp=90
        )
        assert "M140 S90" in result
        assert "M190 S90" in result

    def test_fixed_temp_140_preserved(self):
        result = _resolve_start_gcode(
            FAKE_START_GCODE, hotend_temp=250, bed_temp=90
        )
        assert "M104 S140" in result

    def test_fixed_temp_170_preserved(self):
        result = _resolve_start_gcode(
            FAKE_START_GCODE, hotend_temp=250, bed_temp=90
        )
        assert "M104 S170" in result

    def test_fixed_temp_250_preserved_when_hotend_not_250(self):
        # When hotend IS 250, the M104 S250 line should be left alone
        # (it's a fixed init line, not a parametric one).  But since
        # M104 S250 only gets replaced when hotend_temp != 220 and the
        # line starts with M104 S220, the fixed 250 line is never matched.
        result = _resolve_start_gcode(
            FAKE_START_GCODE, hotend_temp=230, bed_temp=65
        )
        assert "M104 S250" in result

    def test_filament_type_replacement(self):
        result = _resolve_start_gcode(
            FAKE_START_GCODE, filament_type="PETG"
        )
        assert "set_filament_type:PETG" in result
        assert "set_filament_type:UNKNOWN" in result  # UNKNOWN preserved

    def test_filament_type_pla_unchanged(self):
        result = _resolve_start_gcode(
            FAKE_START_GCODE, filament_type="PLA"
        )
        assert "set_filament_type:PLA" in result

    def test_empty_template(self):
        result = _resolve_start_gcode("", hotend_temp=250, bed_temp=90)
        assert result == ""

    def test_both_temps_changed(self):
        result = _resolve_start_gcode(
            FAKE_START_GCODE, hotend_temp=240, bed_temp=80
        )
        assert "M104 S240" in result
        assert "M109 S240" in result
        assert "M140 S80" in result
        assert "M190 S80" in result
        # Fixed temps still present.
        assert "M104 S140" in result
        assert "M104 S170" in result
        assert "M104 S250" in result


