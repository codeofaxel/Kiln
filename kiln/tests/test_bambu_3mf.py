class TestPostprocessPrusaBody:
    """PrusaSlicer gcode post-processing: init stripping + layer tracking."""

    def test_strips_prusa_init_commands(self):
        result = _postprocess_prusa_body(
            MINIMAL_GCODE_BODY, total_layers=2, est_time_sec=60
        )
        lines = result.split("\n")
        # M83, G28, M104, M140 from the header should be stripped.
        stripped_commands = [line.strip() for line in lines]
        assert "M83" not in stripped_commands
        assert "G28" not in stripped_commands

    def test_preserves_comments_in_header(self):
        result = _postprocess_prusa_body(
            MINIMAL_GCODE_BODY, total_layers=2, est_time_sec=60
        )
        assert "; generated by PrusaSlicer" in result

    def test_injects_m73_layer_tracking(self):
        result = _postprocess_prusa_body(
            MINIMAL_GCODE_BODY, total_layers=2, est_time_sec=60
        )
        assert "M73 L1" in result
        assert "M73 L2" in result

    def test_injects_m991_layer_notification(self):
        result = _postprocess_prusa_body(
            MINIMAL_GCODE_BODY, total_layers=2, est_time_sec=60
        )
        assert "M991 S0 P0" in result

    def test_injects_progress_percentage(self):
        result = _postprocess_prusa_body(
            MINIMAL_GCODE_BODY, total_layers=2, est_time_sec=120
        )
        # Layer 1 of 2 = 50%, layer 2 of 2 = 99% (capped)
        assert "M73 P50" in result
        assert "M73 P99" in result

    def test_progress_never_exceeds_99(self):
        # Single layer — 1/1*100 = 100, but capped at 99
        body = ";LAYER_CHANGE\nG1 Z0.2\n"
        result = _postprocess_prusa_body(
            body, total_layers=1, est_time_sec=60
        )
        assert "M73 P99" in result
        assert "M73 P100" not in result

    def test_remaining_time_in_minutes(self):
        result = _postprocess_prusa_body(
            MINIMAL_GCODE_BODY, total_layers=2, est_time_sec=600
        )
        # At layer 1: remaining = (2-1)*600/2 = 300 sec = 5 min
        assert "R5" in result

    def test_remaining_time_minimum_one_minute(self):
        result = _postprocess_prusa_body(
            MINIMAL_GCODE_BODY, total_layers=2, est_time_sec=60
        )
        # At layer 2: remaining sec = max(60, (2-2)*60/2) = max(60,0) = 60 → 1 min
        lines = result.split("\n")
        # Last progress line for layer 2 should have R1
        r_values = [line for line in lines if line.startswith("M73 P") and "R" in line]
        assert any("R1" in v for v in r_values)

    def test_preserves_gcode_after_header(self):
        result = _postprocess_prusa_body(
            MINIMAL_GCODE_BODY, total_layers=2, est_time_sec=60
        )
        assert "G1 X10 Y10 E0.5" in result
        assert "G1 X20 Y20 E0.5" in result

    def test_layer_comment_format(self):
        result = _postprocess_prusa_body(
            MINIMAL_GCODE_BODY, total_layers=2, est_time_sec=60
        )
        assert "; layer num/total_layer_count: 1/2" in result
        assert "; layer num/total_layer_count: 2/2" in result
        assert "; update layer progress" in result

    def test_no_layer_change_markers(self):
        body = "; just comments\nG1 X10\n"
        result = _postprocess_prusa_body(
            body, total_layers=0, est_time_sec=0
        )
        # No injection, body passes through.
        assert "M73 L" not in result
        assert "G1 X10" in result

    def test_empty_body(self):
        result = _postprocess_prusa_body(
            "", total_layers=0, est_time_sec=0
        )
        assert result == ""

    def test_non_init_gcode_before_first_layer_preserved(self):
        """Non-init commands before the first LAYER_CHANGE should be kept."""
        body = "G92 E0\nG1 F1800\n;LAYER_CHANGE\nG1 Z0.2\n"
        result = _postprocess_prusa_body(
            body, total_layers=1, est_time_sec=30
        )
        # G92 is in _skip_prefixes so it IS stripped.
        # G1 F1800 is NOT in _skip_prefixes, so it should be kept.
        assert "G1 F1800" in result


