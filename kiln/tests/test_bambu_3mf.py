class TestBuildBambu3mfSourceExtraction:
    """Thumbnail and geometry extraction from a source 3MF."""

    def _mock_templates(self):
        return (
            patch(
                "kiln.printers.bambu_3mf._load_a1_start_gcode",
                return_value=FAKE_START_GCODE,
            ),
            patch(
                "kiln.printers.bambu_3mf._load_a1_end_gcode",
                return_value=FAKE_END_GCODE,
            ),
        )

    def test_extracts_thumbnails_from_source(self, tmp_path):
        # Create a fake source 3MF with a thumbnail.
        source = str(tmp_path / "source.3mf")
        with zipfile.ZipFile(source, "w") as zf:
            zf.writestr("Metadata/thumbnail.png", b"fake_png_data")
            zf.writestr("3D/3dmodel.model", "<model>custom geometry</model>")

        out = str(tmp_path / "output.3mf")
        p_start, p_end = self._mock_templates()
        with p_start, p_end:
            build_bambu_3mf(
                MINIMAL_GCODE_BODY, out, source_3mf_path=source
            )
        with zipfile.ZipFile(out) as zf:
            assert "Metadata/thumbnail.png" in zf.namelist()
            assert zf.read("Metadata/thumbnail.png") == b"fake_png_data"

    def test_extracts_3d_model_from_source(self, tmp_path):
        source = str(tmp_path / "source.3mf")
        with zipfile.ZipFile(source, "w") as zf:
            zf.writestr("3D/3dmodel.model", "<model>custom geometry</model>")

        out = str(tmp_path / "output.3mf")
        p_start, p_end = self._mock_templates()
        with p_start, p_end:
            build_bambu_3mf(
                MINIMAL_GCODE_BODY, out, source_3mf_path=source
            )
        with zipfile.ZipFile(out) as zf:
            model = zf.read("3D/3dmodel.model").decode("utf-8")
            assert "custom geometry" in model

    def test_bad_source_3mf_uses_minimal_model(self, tmp_path):
        # Create a corrupt source file.
        source = str(tmp_path / "corrupt.3mf")
        Path(source).write_text("not a zip")

        out = str(tmp_path / "output.3mf")
        p_start, p_end = self._mock_templates()
        with p_start, p_end:
            build_bambu_3mf(
                MINIMAL_GCODE_BODY, out, source_3mf_path=source
            )
        # Should still produce a valid 3MF with the minimal model.
        assert zipfile.is_zipfile(out)
        with zipfile.ZipFile(out) as zf:
            model = zf.read("3D/3dmodel.model").decode("utf-8")
            assert "vertex" in model  # minimal cube model

    def test_nonexistent_source_3mf_uses_minimal_model(self, tmp_path):
        out = str(tmp_path / "output.3mf")
        p_start, p_end = self._mock_templates()
        with p_start, p_end:
            build_bambu_3mf(
                MINIMAL_GCODE_BODY, out,
                source_3mf_path="/nonexistent/source.3mf",
            )
        assert zipfile.is_zipfile(out)

