class TestResolveEndGcode:
    """Z-height substitution in end gcode template."""

    def test_default_z_adjustment(self):
        result = _resolve_end_gcode(FAKE_END_GCODE, max_z=65.0)
        # safe_z = 65.0 + 5.0 = 70.0
        assert "G1 Z70.0 F900" in result

    def test_custom_z_height(self):
        result = _resolve_end_gcode(FAKE_END_GCODE, max_z=20.0)
        # safe_z = 20.0 + 5.0 = 25.0
        assert "G1 Z25.0 F900" in result

    def test_only_first_z_f900_replaced(self):
        template = "G1 Z65.0 F900\nG1 Z10.0 F900\n"
        result = _resolve_end_gcode(template, max_z=30.0)
        # Only the first match replaced.
        assert "G1 Z35.0 F900" in result
        assert "G1 Z10.0 F900" in result

    def test_non_matching_lines_preserved(self):
        result = _resolve_end_gcode(FAKE_END_GCODE, max_z=10.0)
        assert "M104 S0" in result
        assert "M140 S0" in result

    def test_z_with_integer_in_template(self):
        template = "G1 Z50 F900\n"
        result = _resolve_end_gcode(template, max_z=40.0)
        assert "G1 Z45.0 F900" in result


